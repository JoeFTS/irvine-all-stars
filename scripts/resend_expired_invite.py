def send_email(to_email: str, html: str) -> dict:
    payload = {
        "from": "Irvine All-Stars <AllStars@irvineallstars.com>",
        "reply_to": "AllStars@irvinepony.com",
        "to": [to_email],
        "subject": "Your Irvine All-Stars invite link (refreshed)",
        "html": html,
    }
    req = Request(
        "https://api.resend.com/emails",
        data=json.dumps(payload).encode(),
        headers={
            "Authorization": f"Bearer {RESEND}",
            "Content-Type": "application/json",
            "User-Agent": "curl/8.7.1",
        },
        method="POST",
    )
    try:
        with urlopen(req, timeout=30) as r:
            return json.loads(r.read().decode())
    except HTTPError as e:
        raise RuntimeError(f"Resend HTTP {e.code}: {e.read().decode()}")


def team_name_by_id(team_id: str | None, cache: dict) -> str | None:
    if not team_id:
        return None
    if team_id in cache:
        return cache[team_id]
    rows = supa("GET", f"teams?id=eq.{team_id}&select=team_name")
    name = rows[0]["team_name"] if rows else None
    cache[team_id] = name
    return name


def resend_one(email: str, *, dry_run: bool, team_cache: dict, preview_to: str | None = None) -> tuple[bool, str]:
    """Returns (sent, reason). If preview_to set, email goes there instead of `email`
    (token still tied to `email`, so the real parent can still use it later)."""
    email = email.strip().lower()
    if has_profile(email):
        return False, "already has profile (signed up)"
    regs = parent_lookup(email)
    if not regs:
        return False, "no selected/alternate registration for this email"

    # primary email match wins; otherwise use first reg for naming context
    primary_regs = [r for r in regs if (r.get("parent_email") or "").lower() == email]
    ctx_reg = primary_regs[0] if primary_regs else regs[0]
    kids = [f"{r['player_first_name']} {r['player_last_name']}" for r in regs]
    team_label = team_name_by_id(ctx_reg.get("team_id"), team_cache)
    parent_name = ctx_reg.get("parent_name") if primary_regs else None

    existing = existing_valid_invite(email)
    if dry_run:
        if existing:
            url = f"{SITE}/auth/invite/{existing['token']}"
            print(f"[DRY] {email}: REUSE existing valid token; kids={kids}; team={team_label}")
            print(f"      URL: {url}")
        else:
            print(f"[DRY] {email}: would create NEW invite; kids={kids}; team={team_label}")
        return False, "dry-run"

    if existing:
        token = existing["token"]
        reused = True
    else:
        invite = create_invite(email, ctx_reg)
        token = invite["token"]
        reused = False

    url = f"{SITE}/auth/invite/{token}"
    label = f"{'REUSED' if reused else 'NEW'} token; kids={kids}; team={team_label}"
    html = build_html(url, parent_name, kids, team_label)
    recipient = preview_to or email
    res = send_email(recipient, html)
    tag = f"[PREVIEW->{recipient}] " if preview_to else ""
    print(f"{tag}SENT  {email}: resend_id={res.get('id')} {label}")
    print(f"      URL: {url}")
    return True, "sent"


def find_all_expired_emails() -> list[str]:
    """Return distinct parent emails (primary + secondary) for selected/alt
    registrations whose owner has no profile and whose newest invite is
    used=false + expired (or has no invites)."""
    regs = supa(
        "GET",
        "tryout_registrations?status=in.(selected,alternate)"
        "&select=parent_email,secondary_parent_email",
    )
    emails: set[str] = set()
    for r in regs:
        for k in ("parent_email", "secondary_parent_email"):
            v = (r.get(k) or "").strip().lower()
            if v:
                emails.add(v)

    now = datetime.now(timezone.utc).isoformat()
    needs = []
    for e in sorted(emails):
        if has_profile(e):
            continue
        # check newest invite
        rows = supa(
            "GET",
            f"invites?email=ilike.{quote(e)}&select=expires_at,used&order=created_at.desc&limit=1",
        )
        if not rows:
            needs.append(e)
            continue
        latest = rows[0]
        if latest["used"]:
            continue
        if latest["expires_at"] < now:
            needs.append(e)
    return needs


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--email", help="single parent email to resend to")
    ap.add_argument("--all-expired", action="store_true", help="resend to every parent without a profile whose latest invite expired")
    ap.add_argument("--dry-run", action="store_true")
    ap.add_argument("--preview-to", help="route emails to this address instead of the real parents (for proofing copy/render)")
    args = ap.parse_args()

    if not args.email and not args.all_expired:
        ap.error("pass --email EMAIL or --all-expired")

    team_cache: dict = {}
    if args.email:
        sent, reason = resend_one(args.email, dry_run=args.dry_run, team_cache=team_cache, preview_to=args.preview_to)
        if not sent and reason != "dry-run":
            print(f"SKIP {args.email}: {reason}")
        return

    targets = find_all_expired_emails()
    print(f"Found {len(targets)} parent(s) needing a fresh invite.\n")

    # In preview mode, only process the first target (one sample email)
    if args.preview_to:
        targets = targets[:1]
        print(f"--preview-to set; sending 1 sample to {args.preview_to}\n")

    sent_count = 0
    skip_count = 0
    for e in targets:
        try:
            sent, reason = resend_one(e, dry_run=args.dry_run, team_cache=team_cache, preview_to=args.preview_to)
            if sent:
                sent_count += 1
            elif reason != "dry-run":
                print(f"SKIP {e}: {reason}")
                skip_count += 1
        except Exception as ex:
            print(f"ERROR {e}: {ex}")
            skip_count += 1
    print(f"\nDone. sent={sent_count} skipped/errored={skip_count} total={len(targets)}")


if __name__ == "__main__":
    main()
