"""Resend an Irvine All-Stars parent invite when the old link expired.

Looks up the parent by email in tryout_registrations (selected/alternate),
creates a fresh invite row (token + 7-day expiry auto-generated by DB),
emails them a branded link via Resend, and prints the copy-paste URL.

Skips parents who already have a profile (already signed up).
Reuses an existing unused + unexpired invite token if one exists.

Usage:
  python3 scripts/resend_expired_invite.py --email foo@bar.com
  python3 scripts/resend_expired_invite.py --email foo@bar.com --dry-run
  python3 scripts/resend_expired_invite.py --all-expired                 # resend to every selected/alt parent without a profile whose invites all expired
  python3 scripts/resend_expired_invite.py --all-expired --dry-run
"""

import argparse
import json
import os
import sys
from datetime import datetime, timezone
from pathlib import Path
from urllib.error import HTTPError
from urllib.parse import quote
from urllib.request import Request, urlopen


HERE = Path(__file__).resolve().parent


def load_env(path: Path) -> dict[str, str]:
    env: dict[str, str] = {}
    if not path.exists():
        return env
    for line in path.read_text().splitlines():
        line = line.strip()
        if not line or line.startswith("#") or "=" not in line:
            continue
        k, v = line.split("=", 1)
        env[k.strip()] = v.strip().strip('"').strip("'")
    return env


ENV = {**os.environ, **load_env(HERE.parent / ".env.local")}
SUPA = ENV["NEXT_PUBLIC_SUPABASE_URL"].rstrip("/")
KEY = ENV["SUPABASE_SERVICE_ROLE_KEY"]
RESEND = ENV["RESEND_API_KEY"]
SITE = "https://irvineallstars.com"


def supa(method: str, path: str, body: dict | list | None = None) -> list[dict]:
    req = Request(
        f"{SUPA}/rest/v1/{path}",
        data=json.dumps(body).encode() if body is not None else None,
        headers={
            "apikey": KEY,
            "Authorization": f"Bearer {KEY}",
            "Accept-Profile": "irvine_allstars",
            "Content-Profile": "irvine_allstars",
            "Content-Type": "application/json",
            "Prefer": "return=representation",
        },
        method=method,
    )
    try:
        with urlopen(req, timeout=30) as r:
            txt = r.read().decode() or "[]"
            return json.loads(txt) if txt.startswith(("[", "{")) else []
    except HTTPError as e:
        raise RuntimeError(f"{method} {path} -> HTTP {e.code}: {e.read().decode()}")


def parent_lookup(email: str) -> list[dict]:
    e = quote(email)
    return supa(
        "GET",
        f"tryout_registrations?status=in.(selected,alternate)"
        f"&or=(parent_email.ilike.{e},secondary_parent_email.ilike.{e})"
        f"&select=id,parent_name,parent_email,secondary_parent_email,player_first_name,player_last_name,division,team_id,current_team",
    )


def has_profile(email: str) -> bool:
    e = quote(email)
    rows = supa("GET", f"profiles?email=ilike.{e}&select=id")
    return bool(rows)


def existing_valid_invite(email: str) -> dict | None:
    e = quote(email)
    rows = supa(
        "GET",
        f"invites?email=ilike.{e}&used=eq.false"
        f"&expires_at=gt.{quote(datetime.now(timezone.utc).isoformat())}"
        f"&select=id,token,expires_at&order=created_at.desc",
    )
    return rows[0] if rows else None


def create_invite(parent_email: str, reg: dict) -> dict:
    body = {
        "email": parent_email,
        "role": "parent",
    }
    if reg.get("division"):
        body["division"] = reg["division"]
    if reg.get("parent_name") and reg["parent_email"].lower() == parent_email.lower():
        body["parent_name"] = reg["parent_name"]
    if reg.get("player_first_name"):
        body["child_first_name"] = reg["player_first_name"]
    if reg.get("player_last_name"):
        body["child_last_name"] = reg["player_last_name"]
    if reg.get("current_team"):
        body["current_team"] = reg["current_team"]
    res = supa("POST", "invites", body)
    return res[0]


